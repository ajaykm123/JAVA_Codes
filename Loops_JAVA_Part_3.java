package java_basics;

public class Loops_JAVA_Part_3 {

	public static void main(String[] args) {
		
//1. While loop
		
		int i=0;
		while (i<=10) {
			//System.out.println(i);
			i=i+1;
		}
		
		int i1=-10;
		while(i1<=10) {
			//System.out.println(i1);
			i1=i1+1;
		}
		
		int i2=0;
		while(i2<=100) {
			//System.out.println(i2);
			i2=i2+1;
		}
		
		int i3=1;
		while(i3<=5) {
			//System.out.println("Ajaya");
			i3=i3+1;
			
		}
		
		int i4= 10;
		while(i4>=0) {
			//System.out.println(i4);
			i4=i4-1;
		}
		
		
	//2. For Loop
		
		
		for(int j=1;j<=10;j++) { // Here the initialization,condition and the increment or decrement is written in the same line.
			//System.out.println(j);
		}
		
		for(int j1=0;j1<=100;j1++) {
			//System.out.println(j1);
		}
		for(int j2=1;j2<=5;j2++) {
			//System.out.println("Ajaya");
		}
		for (int j3=1;j3<=5;j3=j3+1) {
			//System.out.println("Mainali");
		}
		
		for(int j4=10;j4>=1;j4--) {
			//System.out.println(j4);
		}
		for(int j5=-10;j5<=0;j5++) {
			//System.out.println(j5);
			
		}
		for(int j6=10;j6>=-10;j6--) {
			System.out.println(j6);
		}
		
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}


